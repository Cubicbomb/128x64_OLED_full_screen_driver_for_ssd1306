# ESP32 Pin assignment 
i2c = SoftI2C(scl=Pin(1), sda=Pin(3))


oled_width = 128
oled_height = 64
oled = ssd1306.SSD1306_I2C(oled_width, oled_height, i2c)

def _display_128x64(oled, num):
    code = 0x00
    byte_data =face[num]#读取utf8编码为num的点阵编码
    for t in range(0, 16):
        for y in range(0, 64):#y是行数
            a = bin(byte_data[y+64*t]).replace('0b', '')
            while len(a) < 8:
                a = '0' + a
            for x in range(0, 8):
                oled.pixel(x + 8*t, y, int(a[x]))

#我写的就是一坨垃圾

oled.fill(0)
_display_128x64(oled, 1)
oled.show()